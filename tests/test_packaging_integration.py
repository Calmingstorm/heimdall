class TestDefaultConfigImmutability:
    """Verify _DEFAULT_CONFIG is not mutated by build_config calls."""

    def test_repeated_calls_independent(self):
        """Multiple build_config calls don't interfere with each other."""
        cfg1 = build_config(
            hosts={"a": {"address": "1.2.3.4"}},
            features={"browser": True},
            web_api_token="token1",
        )
        cfg2 = build_config(
            hosts={"b": {"address": "5.6.7.8"}},
            features={"voice": True},
            web_api_token="token2",
        )

        # Verify independence
        assert "a" in cfg1["tools"]["hosts"]
        assert "b" not in cfg1["tools"]["hosts"]
        assert "b" in cfg2["tools"]["hosts"]
        assert "a" not in cfg2["tools"]["hosts"]

        assert cfg1["browser"]["enabled"] is True
        assert cfg2["browser"]["enabled"] is False
        assert cfg2["voice"]["enabled"] is True
        assert cfg1["voice"]["enabled"] is False

    def test_default_config_unchanged_after_build(self):
        """_DEFAULT_CONFIG is unchanged after build_config calls."""
        original_hosts = copy.deepcopy(_DEFAULT_CONFIG["tools"]["hosts"])
        original_browser = _DEFAULT_CONFIG["browser"]["enabled"]

        build_config(
            hosts={"x": {"address": "10.0.0.1"}},
            features={"browser": True},
        )

        assert _DEFAULT_CONFIG["tools"]["hosts"] == original_hosts
        assert _DEFAULT_CONFIG["browser"]["enabled"] == original_browser
