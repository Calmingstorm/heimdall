class TestWebCliWizardSharedLogic:
    """Verify web and CLI wizards share the same config generation logic."""

    def test_web_api_imports_from_setup_wizard(self):
        """api.py imports build_config, build_env from setup_wizard."""
        api_content = (SRC_DIR / "web" / "api.py").read_text()
        assert "from ..setup_wizard import" in api_content
        assert "build_config" in api_content
        assert "build_env" in api_content
        assert "is_setup_needed" in api_content
        assert "validate_token_format" in api_content

    def test_web_and_cli_build_config_produce_same_structure(self):
        """build_config with same args produces same structure regardless of caller."""
        hosts = {"srv": {"address": "10.0.0.1", "ssh_user": "deploy"}}
        features = {"browser": True, "voice": False, "comfyui": True}

        cfg = build_config(
            timezone="US/Eastern",
            hosts=hosts,
            features=features,
            web_api_token="tok123",
            claude_code_host="srv",
        )

        # All 16 sections present
        expected_sections = [
            "discord", "openai_codex", "context", "sessions", "tools",
            "webhook", "learning", "search", "logging", "usage",
            "voice", "browser", "monitoring", "permissions", "comfyui", "web",
        ]
        for section in expected_sections:
            assert section in cfg, f"Missing section: {section}"

        # Features applied
        assert cfg["browser"]["enabled"] is True
        assert cfg["voice"]["enabled"] is False
        assert cfg["comfyui"]["enabled"] is True

        # Hosts applied
        assert cfg["tools"]["hosts"]["srv"]["address"] == "10.0.0.1"
        assert cfg["tools"]["claude_code_host"] == "srv"
        assert cfg["web"]["api_token"] == "tok123"
        assert cfg["timezone"] == "US/Eastern"

    def test_build_env_produces_valid_env_format(self):
        """build_env output is parseable as env file."""
        content = build_env("test.token.value", extra={"EXTRA_VAR": "extra_val"})
        env_vars = {}
        for line in content.splitlines():
            stripped = line.strip()
            if stripped.startswith("#") or not stripped:
                continue
            key, _, val = stripped.partition("=")
            env_vars[key] = val

        assert env_vars["DISCORD_TOKEN"] == "test.token.value"
        assert env_vars["EXTRA_VAR"] == "extra_val"

    def test_is_setup_needed_shared_between_web_and_cli(self, tmp_path):
        """is_setup_needed function works for both web and CLI wizards."""
        config_path = tmp_path / "config.yml"
        env_path = tmp_path / ".env"

        # No files = setup needed
        assert is_setup_needed(config_path, env_path) is True

        # Create config but no env
        config_path.write_text("discord:\n  token: test\n")
        assert is_setup_needed(config_path, env_path) is True

        # Create env with real token
        env_path.write_text("DISCORD_TOKEN=real.token.here\n")
        assert is_setup_needed(config_path, env_path) is False


