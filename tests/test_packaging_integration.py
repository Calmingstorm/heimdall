class TestPackagingWizardConsistency:
    """Verify packaging scripts and CLI wizard are consistent."""

    def test_postinstall_wizard_path_references_setup_module(self):
        """postinstall.sh references the correct Python module for setup."""
        content = (PACKAGING_DIR / "postinstall.sh").read_text()
        assert "src.setup" in content, \
            "postinstall must reference 'src.setup' module for wizard"

    def test_postinstall_port_matches_config_default(self):
        """postinstall.sh web wizard URL port matches config schema default."""
        from src.config.schema import WebConfig
        default_port = WebConfig.model_fields["port"].default

        content = (PACKAGING_DIR / "postinstall.sh").read_text()
        assert f"localhost:{default_port}" in content, \
            f"postinstall must reference port {default_port} (WebConfig default)"

    def test_wizard_config_sections_match_default_config(self):
        """Wizard _DEFAULT_CONFIG has same sections as build_config output."""
        cfg = build_config()
        for section in _DEFAULT_CONFIG:
            assert section in cfg, \
                f"build_config missing section from _DEFAULT_CONFIG: {section}"

    def test_wizard_generated_config_valid_yaml(self):
        """Config generated by wizard is valid YAML round-trip."""
        cfg = build_config(
            hosts={"srv": {"address": "10.0.0.1", "ssh_user": "root"}},
            features={"browser": True, "voice": False, "comfyui": False},
            web_api_token="test-token",
        )
        yaml_str = yaml.dump(cfg, default_flow_style=False, sort_keys=False)
        restored = yaml.safe_load(yaml_str)
        assert restored["discord"]["token"] == cfg["discord"]["token"]
        assert restored["tools"]["hosts"]["srv"]["address"] == "10.0.0.1"
        assert restored["browser"]["enabled"] is True

    def test_wizard_env_output_matches_postinstall_env_location(self):
        """Wizard generates .env that postinstall would place in /etc/heimdall/."""
        env_content = build_env("test.token.value")
        assert "DISCORD_TOKEN=test.token.value" in env_content
        # Verify format matches what postinstall copies from .env.example
        lines = env_content.strip().splitlines()
        for line in lines:
            if line.startswith("#") or not line.strip():
                continue
            assert "=" in line, f"Non-comment env line missing '=': {line}"

    def test_postinstall_creates_dirs_for_default_config_paths(self):
        """postinstall.sh creates directories referenced in _DEFAULT_CONFIG."""
        content = (PACKAGING_DIR / "postinstall.sh").read_text()
        ops = extract_script_operations(content)

        # _DEFAULT_CONFIG references ./data/sessions, ./data/context, etc.
        # postinstall creates these under /var/lib/heimdall/
        for subdir in DATA_SUBDIRS:
            assert any(subdir in line for line in ops["directory_creation"]), \
                f"postinstall must create data subdir '{subdir}'"


